function openGame(game) {

  if (game === "flappy") {
    window.location.href = "flappy.html";
  }

  if (game === "memory") {
    window.location.href = "memory.html";
  }

  if (game === "shooter") {
    window.location.href = "shooter.html";
  }

  if (game === "story") {
    window.location.href = "story.html";
  }

}
